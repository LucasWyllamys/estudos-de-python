'''
Método                Descrição
.isnumeric()          Retorna True se o valor é numérico.
.isalpha()            Retorna True se o valor é um caractere.
.isalnum()            Retorna True se o valor for alfanumérico.
.isupper()            Retorna True se a string estiver toda em letras maiúsculas.
.islower()            Retorna True se a string estiver toda em letras minúsculas.
.isspace()            Retorna True se a string for apenas espaços.
.isdigit()            Retorna True se o valor é apenas composto por dígitos de 0 a 9.
.istitle()            Retorna True se o valor for capitalizado (primeira letra maiúscula e demais minúsculas).
'''