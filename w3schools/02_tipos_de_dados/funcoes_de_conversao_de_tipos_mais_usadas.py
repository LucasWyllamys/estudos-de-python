# Funções de conversão de tipos mais usadas no Python (definindo o tipo da variável)
a = str('Olá, Mundo!')                      # Tipo: str                          
b = int(20)                                 # Tipo: int
c = float(20.5)                             # Tipo: float
d = bool(5)                                 # Tipo: bool
e = list(("apple", "banana", "cherry"))     # Tipo: list
f = tuple(("apple", "banana", "cherry"))    # Tipo: tuple
g = dict(name="John", age=36)               # Tipo: dict (dicionário)