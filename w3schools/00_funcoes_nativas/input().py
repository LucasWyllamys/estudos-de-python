# Python permite entrada de dados pelo usuário.
# Isso significa que podemos pedir informações ao usuário.
# O exemplo a seguir solicita o nome de usuário no terminal e, quando você o digita, ele é impresso na tela:
username = input("Digite o nome de usuário: ")  # Entrada: LucasWyllamys
print(f"O nome de usuário é: {username}")       # Saída: O nome de usuário é: LucasWyllamys
# O Python para de executar quando chega à função input() e continua quando o usuário fornece alguma entrada.