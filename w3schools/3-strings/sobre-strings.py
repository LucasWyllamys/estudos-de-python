# Strings em Python são delimitadas por aspas simples ou duplas.
print('Olá, Mundo!')  # Saída: Olá, Mundo!
print("Olá, Mundo!")  # Saída: Olá, Mundo!

# Como concatenar strings em Python
# Use o + para concatenar strings
nome = "Lucas"
sobrenome = "Wyllamys"
nomeCompleto = nome + " " + sobrenome
print(nomeCompleto)   # Saída: Lucas Wyllamys