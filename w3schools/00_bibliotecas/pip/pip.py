# pip: 
    # É um gerenciador de pacotes Python, ou módulos, se preferir.
    # Observação: se você tiver o Python versão 3.4 ou posterior, o PIP será incluído por padrão.

# Pacote: 
    # Contém todos os arquivos necessários para um módulo.

# Módulos: 
    # São bibliotecas de código Python que você pode incluir em seu projeto. 

# Verifica a versão do pip: 
    # pip --version

# Instala um pacote:
    # pip install selenium
    # pip install selenium --proxy=10.219.78.3:8080

# Importa um módulo:
    # import selenium
 
# Desintala um pacote:
    # pip uninstall selenium

# Use o comando list para listar todos os pacotes instalados no seu sistema:
    # pip list