''' Metacaracteres são caracteres com um significado especial:
Caracter    Descrição
[]          Um conjunto de caracteres.        
/           Sinaliza uma sequência especial (também pode ser usado para escapar caracteres especiais).           
.           Representa qualquer caracter, exceto \n.
^	        Começa com.	
$	        Termina com.
*	        Zero ou mais ocorrências.
+	        Uma ou mais ocorrências.
?	        Zero ou uma ocorrência.
{}	        Exatamente o número especificado de ocorrências.
|	        Ou.
()	        Capturar e agrupar.
'''