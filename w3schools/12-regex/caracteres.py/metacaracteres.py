''' Metacaracteres são caracteres com um significado especial:
Caracter    Descrição
[]          Um conjunto de caracteres (e/ou conjunto de possibilidades).        
\           Sinaliza uma sequência especial (também pode ser usado para escapar caracteres especiais).           
.           Representa qualquer caracter, exceto \n.
^	        Começa com.	
$	        Termina com.
*	        Especifica zero ou mais ocorrências.
+	        Especifica uma ou mais ocorrências.
?	        Especifica zero ou uma ocorrência.
{}	        Especifica o número de ocorrências.
|	        Ou.
()	        Capturar e agrupar.
'''