# O método "remove()"" remove o item especificado.
myList = ['maçã', 'banana', 'morango']
myList.remove('banana')     # Remove o item 'banana' da lista "myList"
print(myList)               # Saída: ['maçã', 'morango']
# Obs.: Se houver mais de um item com o valor especificado, o método remove() remove a primeira ocorrência.
