# A função range no Python é usada para gerar uma sequência de números. É muito útil em loops, especialmente em loops for, onde você precisa iterar um número específico de vezes. Aqui estão alguns exemplos de como ela pode ser usada:

# Sequência Simples:
for i in range(5):
    print(i)
''' Saída: 
0
1
2
3
4
'''

# Sequência com Início e Fim Específicos:
for i in range(2, 7):
    print(i)
''' Saída:
2
3
4
5
6
'''

# Sequência com Passo Específico:
for i in range(1, 10, 2):
    print(i)
''' Saída:
1
3
5
7
9
'''
